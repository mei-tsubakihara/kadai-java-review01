
public class IfSample1 {

    public static void main(String[] args) {
        int score = -10; //成績
        
        //scoreが60以上ならば
        if(score >= 60) {
            //合格です!と表示
            System.out.println("合格です!");
        } else {
            System.out.println("赤点です…");
        }

    }

}
